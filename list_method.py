#sort method 
L1 = [1,8,7,2,12,43]
print(L1)
L1.sort()
print(L1)

#reverse method 
L1.reverse()
print(L1)

#append method 
#it is use to add a integer at the end of lists
L1.append(23)
print(L1)
L1.append(34)
print(L1)

#insert method

L1.insert(2,10)
print(L1)

#pop method used to delete integer at index
L1.pop(2)
print(L1)

#remove method is used to remove 15 from list
L1.remove(43)
print(L1)